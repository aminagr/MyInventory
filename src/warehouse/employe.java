       public void tb_load(){
           
          
    try {
        DefaultTableModel dt = (DefaultTableModel) jTable1.getModel();
        dt.setRowCount(0);

        UserDAO userDao = new UserDAO();
        List<User> userList = userDao.listAllUsers();

        for (User user : userList) {
            Object[] rowData = {
                    user.getId(),
                    user.getUsername(),
                    user.getNom(),
                    user.getPrenom(),
                    user.getEmail(),
                    user.getPhoneNumber(),
                    user.getAddress()
            };
            dt.addRow(rowData);
        }
    } catch (Exception e) {
        System.out.println(e);
    }
}


  
           
           
       
       
       
       
       
       
