    private void updateTable(List<User> userList) {
    DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
    model.setRowCount(0); // Clear existing rows

    for (User user : userList) {
        model.addRow(new Object[]{
            user.getId(),
            user.getUsername(),
            user.getNom(),
            user.getPrenom(),
            user.getEmail(),
            user.getPhoneNumber(),
            user.getAddress()
        });
    }
}
    
    
